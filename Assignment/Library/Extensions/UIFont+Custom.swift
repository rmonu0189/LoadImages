import Foundation
import UIKit

extension UIFont {
    
    static func museo300(_ size: CGFloat) -> UIFont {
        return UIFont(name: "Museo-300", size: size)!
    }
    
    static func museo500(_ size: CGFloat) -> UIFont {
        return UIFont(name: "Museo500-Regular", size: size)!
    }
    
}
